#EJERCICIOS LISTAS

# Metodos propios

lista = [45,32,3,78]
print("Lista original:", lista)
# Funcion append: añade un elemento  a la lista
lista.append(995)
lista.append(7)
print("Lista despues de usar appen:", lista)
# Funcion sort: ordena la lista
lista.sort
print("Lista ordenada:", lista)
# Funcion reverse: Invierte el orden de la lista
lista.reverse()
print("Lista al reves:",lista)
# Funcion extend: Añade los elementos de una lista a lista
lista_extend = [1,5,87,45]
lista.extend(lista_extend)
print("Lista despues de extend:",lista)
# Funcion  count: cuenta el numero de veces que aparece el elemnto indicado como parametro dentro de la lista
print("Numero de lementos 45:", lista.count(45))
#funcion insert: Añade el elemento pasado como parametro a la lista en la posicion indicada tambien por el parametro
lista.insert[4,111]
print("lista despues de insert:",lista)
# Funcion remove:elimin la primera ocurriencia empezando por la izquierda de la lista del elemento indicado por el parametro.
lista.remove(45)
print("Lista despues de remove:",lista)
#Funcion index:devuelve la posicion de la  primera izquierda a derecha en la lista, del elemento pasado como parametro
print("Posicion del elemento 111:", lista.index(111))
# Funcion pop: Elimina un elemento de la lista y lo devuelve como resultado de la operacion.
lista.pop()
print("lista despues de pop: ", lista)
# Funcion clear: elimina todos los elementos de la lista 
lista.clear()
print("Lista despues de clear: ", lista)

